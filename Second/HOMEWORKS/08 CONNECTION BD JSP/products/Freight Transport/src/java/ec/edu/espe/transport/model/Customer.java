package ec.edu.espe.transport.model;
/**
 *
 * @author dennise
 */
public class Customer {
    private String idCard;
    private String ruc;
    private String name;
    private String address;
    private String telephone;
    private String phone;
    private String email;

    public Customer() {
    }

    public Customer(String idCard, String ruc, String name, String address, String telephone, String phone, String email) {
        this.idCard = idCard;
        this.ruc = ruc;
        this.name = name;
        this.address = address;
        this.telephone = telephone;
        this.phone = phone;
        this.email = email;
    }

    /**
     * @return the idCard
     */
    public String getIdCard() {
        return idCard;
    }

    /**
     * @param idCard the idCard to set
     */
    public void setIdCard(String idCard) {
        this.idCard = idCard;
    }

    /**
     * @return the ruc
     */
    public String getRuc() {
        return ruc;
    }

    /**
     * @param ruc the ruc to set
     */
    public void setRuc(String ruc) {
        this.ruc = ruc;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the telephone
     */
    public String getTelephone() {
        return telephone;
    }

    /**
     * @param telephone the telephone to set
     */
    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Customer{" + "idCard=" + idCard + ", ruc=" + ruc + ", name=" + name + ", address=" + address + ", telephone=" + telephone + ", phone=" + phone + ", email=" + email + '}';
    }

    
}
